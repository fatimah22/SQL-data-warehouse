PRINT 'EDA: 5-Magnitude Exploration';
PRINT '====================================';
-- Find the total customers by countries
SELECT country,
COUNT (customer_key) AS Total_Customers
FROM gold.dim_customers
GROUP BY country 
ORDER BY Total_Customers DESC

-- Find the total customers by gender 
SELECT gender,
COUNT (customer_key) AS Total_Customers
FROM gold.dim_customers
GROUP BY gender
ORDER BY Total_Customers DESC

-- Find total products by category
SELECT category,
COUNT (product_key) AS Total_Products
FROM gold.dim_products
GROUP BY category 
ORDER BY Total_Products DESC

-- What is the avevrage costs in each category?
SELECT 
category,
AVG (cost) AS Average_Cost
FROM gold.dim_products
GROUP BY category
ORDER BY Average_Cost DESC

-- What is the total revenue generatd for each category?
SELECT 
p.category,
SUM (s.sales_amount) AS Total_Revenue
FROM gold.fact_sales s
LEFT JOIN  gold.dim_products P
ON s.product_key = p.product_key
GROUP BY category
ORDER BY Total_Revenue DESC

-- Find total revenue is generated by each customer
SELECT 
c.customer_key,
c.first_name,
c.last_name,
SUM (s.sales_amount) AS Total_Revenue
FROM gold.fact_sales s
LEFT JOIN  gold.dim_customers c
ON s.customer_key = c.customer_key
GROUP BY 
c.customer_key,
c.first_name,
c.last_name
ORDER BY Total_Revenue DESC

-- What is the distribution of sold items across countries?
SELECT 
c.country,
SUM (s.quantity) AS Total_Sold_Items
FROM gold.fact_sales s
LEFT JOIN  gold.dim_customers c
ON s.customer_key = c.customer_key
GROUP BY c.country
ORDER BY Total_Sold_Items DESC

