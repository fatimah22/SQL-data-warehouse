-- Explore the date of first and last order 
-- How many years of sales are avaliable
SELECT 
MIN(order_date) AS FirstOrderDate,
MAX(order_date) AS LastOrderDate,
DATEDIFF(YEAR,MIN(order_date),MAX(order_date)) AS OrderRangeYears
FROM gold.fact_sales


-- Find the youngest and oldest customer
SELECT 
MIN(birthdate) OldestCustomer ,
MAX(birthdate) YoungestCustomer,
DATEDIFF(YEAR, MIN(birthdate),GETDATE()) AS OldestCustomerAge ,
DATEDIFF(YEAR, MAX(birthdate),GETDATE()) AS YoungestCustomerAge 
