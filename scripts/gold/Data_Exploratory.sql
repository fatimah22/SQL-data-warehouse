PRINT 'EDA: 6-Ranking Exploration';
PRINT '====================================';
-- Which 5 products generate teh higest revenu ?
SELECT TOP 5
p.product_name,
SUM (s.sales_amount) AS Total_Revenue
FROM gold.fact_sales s
LEFT JOIN  gold.dim_products P
ON s.product_key = p.product_key
GROUP BY p.product_name
ORDER BY Total_Revenue DESC
-- Same task using window function 
SELECT * 
FROM (
	SELECT 
	p.product_name,
	SUM (s.sales_amount) AS Total_Revenue,
	ROW_NUMBER() OVER (ORDER BY SUM (s.sales_amount)) AS Rank_Products
	FROM gold.fact_sales s
	LEFT JOIN  gold.dim_products P
	ON s.product_key = p.product_key
	GROUP BY p.product_name
	) t
WHERE Rank_Products <=5

-- What are the  5 worset-performing products in terms of sales ?
SELECT TOP 5
p.product_name,
SUM (s.sales_amount) AS Total_Revenue
FROM gold.fact_sales s
LEFT JOIN  gold.dim_products P
ON s.product_key = p.product_key
GROUP BY p.product_name
ORDER BY Total_Revenue ASC

